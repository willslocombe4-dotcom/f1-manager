def get_all_circuits():
    """Get a list of all circuit IDs"""
    return list(CIRCUITS.keys())


def get_circuit_by_id(circuit_id):
    """
    Get circuit data by ID.

    Args:
        circuit_id: Circuit identifier (e.g., 'monaco', 'silverstone')

    Returns:
        dict: Circuit data or None if not found
    """
    return CIRCUITS.get(circuit_id)


def get_circuit_name(circuit_id):
    """
    Get the full name of a circuit.

    Args:
        circuit_id: Circuit identifier

    Returns:
        str: Full circuit name or None
    """
    circuit = CIRCUITS.get(circuit_id)
    return circuit["name"] if circuit else None


def get_circuit_waypoints(circuit_id):
    """
    Get waypoints for a circuit.

    Args:
        circuit_id: Circuit identifier

    Returns:
        list: List of (x, y) waypoint tuples or None
    """
    circuit = CIRCUITS.get(circuit_id)
    return circuit["waypoints"] if circuit else None


def get_circuit_drs_zones(circuit_id):
    """
    Get DRS zones for a circuit.

    Args:
        circuit_id: Circuit identifier

    Returns:
        list: List of DRS zone dicts with 'start' and 'end' keys or None
    """
    circuit = CIRCUITS.get(circuit_id)
    return circuit["drs_zones"] if circuit else None


def get_tire_degradation_multiplier(circuit_id):
    """
    Get tire degradation multiplier for a circuit.

    Args:
        circuit_id: Circuit identifier

    Returns:
        float: Tire degradation multiplier (0.7 to 1.4) or 1.0 if not found
    """
    circuit = CIRCUITS.get(circuit_id)
    if circuit and "characteristics" in circuit:
        return circuit["characteristics"].get("tire_degradation", 1.0)
    return 1.0


def is_street_circuit(circuit_id):
    """
    Check if a circuit is a street circuit.

    Args:
        circuit_id: Circuit identifier

    Returns:
        bool: True if street circuit, False otherwise
    """
    circuit = CIRCUITS.get(circuit_id)
    return circuit["type"] == "street" if circuit else False
